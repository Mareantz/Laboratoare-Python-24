def convert_to_bin(value):
    result = ''
    while value:
        result = str(value % 2) + result
        value //= 2
    result = "0" * (4 - len(result)) + result
    return result


def font(list_of_hex):
    result = ''
    for i in range(len(list_of_hex[0])):
        line = ''
        for j in range(len(list_of_hex)):
            l_4 = list_of_hex[j][i] // 16
            r_4 = list_of_hex[j][i] % 16
            line = line + convert_to_bin(l_4) + convert_to_bin(r_4)
        result = result + line + '\n'
    return result
